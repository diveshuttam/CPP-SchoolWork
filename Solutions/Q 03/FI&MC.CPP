FI convert(MC a,FI b)
{
  float temp;
  FI t;
  temp=((a.m*100.0)+(a.c))/2.54+(b.f*12.0)+(b.i); //adding by cnverting into inches
  t.f=temp/2.0;
  t.i=fmod(temp,12);
  return t;
}
